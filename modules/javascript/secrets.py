"""
JavaScript Secret Patterns

Regex database used by the
JavaScript Secret Detection module.
"""

import re


# ==========================================================
# Secret Patterns
# ==========================================================

SECRET_PATTERNS = {

    # ------------------------------------------------------
    # AWS
    # ------------------------------------------------------

    "aws_access_key": re.compile(
        r"\bAKIA[0-9A-Z]{16}\b"
    ),

    "aws_secret_key": re.compile(
        r"\b[A-Za-z0-9/+=]{40}\b"
    ),

    # ------------------------------------------------------
    # Google
    # ------------------------------------------------------

    "google_api_key": re.compile(
        r"\bAIza[0-9A-Za-z\-_]{35}\b"
    ),

    # ------------------------------------------------------
    # GitHub
    # ------------------------------------------------------

    "github_token": re.compile(
        r"\bgh[pousr]_[A-Za-z0-9]{36,255}\b"
    ),

    # ------------------------------------------------------
    # GitLab
    # ------------------------------------------------------

    "gitlab_token": re.compile(
        r"\bglpat-[A-Za-z0-9\-_]{20,255}\b"
    ),

    # ------------------------------------------------------
    # Slack
    # ------------------------------------------------------

    "slack_token": re.compile(
        r"\bxox[baprs]-[A-Za-z0-9-]{10,200}\b"
    ),

    # ------------------------------------------------------
    # Stripe
    # ------------------------------------------------------

    "stripe_live_key": re.compile(
        r"\bsk_live_[A-Za-z0-9]{24,}\b"
    ),

    "stripe_test_key": re.compile(
        r"\bsk_test_[A-Za-z0-9]{24,}\b"
    ),

    # ------------------------------------------------------
    # Firebase
    # ------------------------------------------------------

    "firebase_key": re.compile(
        r"\bAAAA[A-Za-z0-9_-]{7,}\b"
    ),

    # ------------------------------------------------------
    # Twilio
    # ------------------------------------------------------

    "twilio_sid": re.compile(
        r"\bAC[a-fA-F0-9]{32}\b"
    ),

    # ------------------------------------------------------
    # OpenAI
    # ------------------------------------------------------

    "openai_key": re.compile(
        r"\bsk-[A-Za-z0-9]{32,}\b"
    ),

    # ------------------------------------------------------
    # JWT
    # ------------------------------------------------------

    "jwt": re.compile(
        r"\beyJ[A-Za-z0-9_\-]+"
        r"\.[A-Za-z0-9_\-]+"
        r"\.[A-Za-z0-9_\-]+\b"
    ),

    # ------------------------------------------------------
    # Bearer Token
    # ------------------------------------------------------

    "bearer_token": re.compile(
        r"Bearer\s+[A-Za-z0-9\-._~+/]+=*",
        re.IGNORECASE,
    ),

    # ------------------------------------------------------
    # Basic Authorization
    # ------------------------------------------------------

    "basic_auth": re.compile(
        r"Basic\s+[A-Za-z0-9+/=]+",
        re.IGNORECASE,
    ),

    # ------------------------------------------------------
    # RSA Private Key
    # ------------------------------------------------------

    "rsa_private_key": re.compile(
        r"-----BEGIN RSA PRIVATE KEY-----"
        r".*?"
        r"-----END RSA PRIVATE KEY-----",
        re.DOTALL,
    ),

    # ------------------------------------------------------
    # Private Key
    # ------------------------------------------------------

    "private_key": re.compile(
        r"-----BEGIN PRIVATE KEY-----"
        r".*?"
        r"-----END PRIVATE KEY-----",
        re.DOTALL,
    ),

    # ------------------------------------------------------
    # EC Private Key
    # ------------------------------------------------------

    "ec_private_key": re.compile(
        r"-----BEGIN EC PRIVATE KEY-----"
        r".*?"
        r"-----END EC PRIVATE KEY-----",
        re.DOTALL,
    ),

    # ------------------------------------------------------
    # SSH Private Key
    # ------------------------------------------------------

    "openssh_private_key": re.compile(
        r"-----BEGIN OPENSSH PRIVATE KEY-----"
        r".*?"
        r"-----END OPENSSH PRIVATE KEY-----",
        re.DOTALL,
    ),

    # ------------------------------------------------------
    # Generic API Key
    # ------------------------------------------------------

    "generic_api_key": re.compile(
        r"""(?i)
        (?:api[_-]?key|
           secret|
           access[_-]?key|
           auth[_-]?token)
        \s*
        [:=]
        \s*
        ['"]?
        ([A-Za-z0-9_\-]{16,})
        ['"]?
        """,
        re.VERBOSE,
    ),

}


# ==========================================================
# Supported Secret Types
# ==========================================================

SUPPORTED_SECRETS = tuple(
    SECRET_PATTERNS.keys()
)